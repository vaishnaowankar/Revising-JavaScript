let num1 = "9";
let num2 = 9;

console.log(num1 == num2);
// It will output as true because it only checks the value of variables not the datatype.

console.log(num1 === num2);
// It will output as false because it checks the value of variables as well as the datatype of variables.

// Similar is for != and !==
console.log(num1 != num2);
// It will output as false because it validates both as same

console.log(num1 !== num2);
// It outputs as false.
